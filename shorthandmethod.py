num = int(input("Enter a number here : "))

# if statement
if num % 2 == 0:
#    print("It is an even number")
    print("even")

# ShortHand if statement

if num % 2 == 0 : print("even")

# if-else statement

if num % 2 == 0 :
    print("even")
else:
    print("odd")
    
# ShortHand if-else

print("even") if num % 2 == 0 else print("odd")